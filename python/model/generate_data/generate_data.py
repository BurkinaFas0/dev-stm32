import numpy as np




# Генерируем простые данные для классификации
def generate_data(n_samples=1000):
    X = []                                                          # здесь будут храниться входные данные (сигналы)
    y = []                                                          # здесь будут метки классов (0 или 1)
    
    for _ in range(n_samples):
        if np.random.random() > 0.5:
            # Класс 1: Синусоида
            t = np.linspace(0, 2*np.pi, 20)                         # создает 20 равномерно распределенных точек от 0 до 2π
            signal = np.sin(t) + np.random.normal(0, 0.1, 20)       # вычисляет синус для каждой точки - это чистый сигнал
                                                                    # добавляет небольшой шум 
                                                                    # (гауссов шум со средним 0 и  отклонением 0.1)
                                                                    # Результат: синусоида с небольшими искажениями
            X.append(signal)                                        # сохраняем сигнал
            y.append(1)                                             # помечаем как класс 1
        else:
            # Класс 0: Шум
            signal = np.random.normal(0, 0.5, 20)
            X.append(signal)
            y.append(0)
    
    return np.array(X), np.array(y)










# np.linspace(0, 2*np.pi, 20)- Эта функция создает 20 равномерно распределенных чисел от 0 до 2π (примерно 6.28):
#   [0, 0.33, 0.66, 0.99, 1.32, 1.65, 1.98, 2.31, 2.64, 2.97, 
#    3.30, 3.63, 3.96, 4.29, 4.62, 4.95, 5.28, 5.61, 5.94, 6.28]


# np.sin(t) - для каждого значения t вычисляется синус:
# 0     -> 0
# 0.33  -> 0.324
# 0.66  -> 0.613
# 0.99  -> 0.836
# 1.32  -> 0.968
# 1.65  -> 0.997


